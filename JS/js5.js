function operacion(op,numero,signumero2) {
    let resultado = 0;
   
       switch(op){
       case "+":
           resultado=numero+signumero2;
           break;
       case "-":
           resultado=numero-signumero2;
           break;
       case "*":
           resultado=numero*signumero2;
           break;
       case "/":
           resultado=numero/signumero2;
           break;
   
   
           
       
   } return resultado
   }
   
   